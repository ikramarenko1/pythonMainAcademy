# Задача: Визначення діапазону числа
# Опис: Напишіть програму, яка перевіряє, в якому діапазоні знаходиться введене число (0-50, 51-100 або поза межами).

num = int(input('Введiть число для визначення дiапазону: '))

if 0 <= num <= 50:
    print('Число в I дiапазонi.')
elif 51 <= num <= 100:
    print('Число в II дiапазонi.')
else:
    print('Число поза межами дiапазону.')