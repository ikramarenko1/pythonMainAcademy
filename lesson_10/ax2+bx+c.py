# 9. Задача: Квадратичне рівняння
# Опис: Напишіть програму, яка розв'язує квадратичне рівняння ax^2 + bx + c = 0 за введеними коефіцієнтами a, b, c.

import math

print('Зараз будемо розв\'язувати квадратичне рівняння ax^2 + bx + c = 0')
a = int(input('Введiть число a: '))
b = int(input('Введiть число b: '))
c = int(input('Введiть число c: '))

d = (b ** 2) - (4 * a * c)
sqrt_d = math.sqrt(d)

x1 = (-b - sqrt_d) / (a * 2)
x2 = (-b - sqrt_d) / (a * 2)

print(f'x1 = {x1}')
print(f'x2 = {x2}')
