# Завдання: Створення множини з рядка зі словами
# Дано рядок зі словами, розділеними пробілами. Створіть множину, яка містить усі унікальні слова з цього рядка.
import re

user_str = str(input('Введiть рядок для виводу всіх унікальних слів з цього рядка: '))
words = re.sub(r'[.,?!]', '', user_str).split()

print(set(words))
