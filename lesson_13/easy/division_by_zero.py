# Ділення на 0:
# Напишіть програму, яка приймає два числа від користувача та виводить результат їх ділення.
# Врахуйте випадок ділення на 0, і виведіть відповідне повідомлення.

a, b = int(input('Введiть чисельник: ')), int(input('Введiть знаменник: '))

try:
    result = a / b
    print(f'Результат дiлення: {result}')
except ZeroDivisionError:
    print('Помилка! Дiлення на 0 неможливе')
