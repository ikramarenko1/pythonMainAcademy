# Контроль введення: Напишіть програму, яка виводить на екран повідомлення "Привіт!" лише тоді,
# коли користувач вводить слово "hello". Якщо введення не відповідає цьому слову, виведіть
# повідомлення "Невідомий ввід".

try:
    user_input = input('Введiть "hello": ')

    if user_input != 'hello':
        raise Exception('Невідомий ввід')

    print('Привіт!')

except Exception as e:
    print(e)
