# Конвертер одиниць: Реалізуйте програму-конвертер одиниць довжини, яка перетворює метри в футові.
# Обробте випадок, коли користувач вводить від'ємне число, і виведіть повідомлення "Некоректне значення".

try:
    meters = float(input('Введiть довжину для переведення в фути: '))

    if meters < 0:
        raise ValueError

    feets = meters * 3.281

    print(f'{meters} метрiв = {feets} футiв')

except ValueError:
    print('Введено некоректне значення')
