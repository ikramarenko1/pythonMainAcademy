# Якщо файл існує, вивести повідомлення "Файл існує", в іншому випадку - "Файл не знайдено".
import os


def is_file(filename):
    return "Файл існує!" if os.path.isfile(filename) else "Такого файлу нема.."


print(is_file("output.txt"))  # "Файл існує!"
print(is_file("output123.txt"))  # "Такого файлу нема.."
