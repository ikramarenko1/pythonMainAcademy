# Записати текст у файл
# Опис: Напишіть програму, яка зчитує текст від користувача і записує його у файл
# з заданою назвою. Користувач вводить текст, і програма створює або перезаписує файл з цим текстом.

filename = str(input('Введіть назву файлу, до якого хочете записати текст: '))
text_to_file = str(input('Введіть бажаний текст: '))

with open(filename, 'w') as file:
    file.write(text_to_file)
    print('Успішно!')
